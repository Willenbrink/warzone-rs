// see if a commander is the n'th command droid
#[no_mangle]
pub unsafe extern "C" fn droidIsCommanderNum(mut psDroid: *mut DROID,
                                             mut n: SDWORD) -> BOOL {
    let mut psCurr: *mut DROID = 0 as *mut DROID;
    let mut numLess: SDWORD = 0;
    if (*psDroid).droidType as libc::c_uint !=
           DROID_COMMAND as libc::c_int as libc::c_uint {
        return 0 as libc::c_int
    }
    numLess = 0 as libc::c_int;
    psCurr = apsDroidLists[(*psDroid).player as usize];
    while !psCurr.is_null() {
        if (*psCurr).droidType as libc::c_uint ==
               DROID_COMMAND as libc::c_int as libc::c_uint &&
               (*psCurr).id < (*psDroid).id {
            numLess += 1 as libc::c_int
        }
        psCurr = (*psCurr).psNext
    }
    if numLess == n - 1 as libc::c_int { return 1 as libc::c_int }
    return 0 as libc::c_int;
}
