// Selects all units owned by the player - onscreen toggle.
#[no_mangle]
pub unsafe extern "C" fn selSelectAllUnits(mut player: UDWORD,
                                           mut bOnScreen: BOOL) -> UDWORD {
    let mut psDroid: *mut DROID = 0 as *mut DROID;
    let mut count: UDWORD = 0;
    selDroidDeselect(player);
    /* Go thru' all */
    psDroid = apsDroidLists[player as usize];
    count = 0 as libc::c_int as UDWORD;
    while !psDroid.is_null() {
        /* Do we care about them being on screen? */
        if if bOnScreen != 0 {
               droidOnScreen(psDroid, 0 as libc::c_int)
           } else { 1 as libc::c_int } != 0 {
            /* can select everything except transporters */
            if (*psDroid).droidType as libc::c_uint !=
                   DROID_TRANSPORTER as libc::c_int as libc::c_uint {
                //					psDroid->selected = TRUE;
                SelectDroid(psDroid);
                count = count.wrapping_add(1)
            }
        }
        psDroid = (*psDroid).psNext
    }
    return count;
}
