/* Initialise the object system */
#[no_mangle]
pub unsafe extern "C" fn objInitialise() -> BOOL {
    if objmemInitialise() == 0 { return 0 as libc::c_int }
    return 1 as libc::c_int;
}
/* Shutdown the object system */
#[no_mangle]
pub unsafe extern "C" fn objShutdown() -> BOOL {
    objmemShutdown();
    return 1 as libc::c_int;
}
