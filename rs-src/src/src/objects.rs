#[no_mangle]
pub unsafe extern "C" fn reverseObjectList(mut ppsList:
                                               *mut *mut BASE_OBJECT) {
    let mut psPrev: *mut BASE_OBJECT = 0 as *mut BASE_OBJECT;
    let mut psNext: *mut BASE_OBJECT = 0 as *mut BASE_OBJECT;
    let mut psCurrent: *mut BASE_OBJECT = 0 as *mut BASE_OBJECT;
    let mut psObjList: *mut BASE_OBJECT = 0 as *mut BASE_OBJECT;
    //initialise the pointers
    psObjList = *ppsList;
    psNext = 0 as *mut BASE_OBJECT;
    psPrev = psNext;
    psCurrent = psObjList;
    while !psCurrent.is_null() {
        psNext = (*psCurrent).psNext;
        (*psCurrent).psNext = psPrev;
        psPrev = psCurrent;
        psCurrent = psNext
    }
    //set the list passed in to point to the new top
    *ppsList = psPrev;
}
